qk_tap_dance_action_t tap_dance_actions[] = {
    [TD_LOWER] = ACTION_TAP_DANCE_FN_ADVANCED(NULL, tdlower_pressed, tdlower_reset),
    [TD_RAISE] = ACTION_TAP_DANCE_FN_ADVANCED(NULL, tdraise_pressed, tdraise_reset)
};
